package main

import (
	"fmt"
)

func main() {
	a := 0
	fmt.Println(a)
	a = a | (1 << 3) //在bit3上设置标志位（从bit0开始算）
	fmt.Println(a)
	a = a | (1 << 6) //在bit6上设置标志位

	fmt.Println(a) //a = 72 = 0100 1000

	a = a &^ (1 << 6) //清除bit6上得标志位 a = 8 = 0000 0100
	fmt.Println(a)
}

